# !OPERATOR LOGIKA SHIFTING
# ! 1.SHIFTING RIGHT (>>)
a = 10
b = a >> 1
print('NILAI :', a , ',binary', format(a, '08b'));
print('-' * 30, '>>')
print('NILAI :', b , ' ,binary', format(b, '08b'));
print('=' * 25)

# ! 2.SHIFTING LEFT
a = 2
b = a << 2
print('NILAI :', a , ',binary', format(a, '08b'));
print('-' * 30, '>>')
print('NILAI :', b , ',binary', format(b, '08b'));
print('=' * 25)
